# Create a list

a = [1, 2, 4, 6]

#Print a list
print(a)

#Access using index using a[0], a[1], a[2]
print(a[2])

#Change the value of list a[0]=10, a[2]=20
a[0] = 10
a[2] = 20
print(a)

#We can create a list using different item types
a = [4, "Aditya", 2.5, False]
print(a)

#List Slicing
Name = ("Aditya", "Ankita", 45, "Dhuri")
print(Name[0:1])
print(Name[0:2])
print(Name[::2])