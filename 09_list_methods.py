# List Methods
L1 = [1, 2, 18, 28, 9, 6, 3, 3]

#Sort the list into ascending order and change it
L1.sort()
print(L1)

#Reverse the list
L1.reverse()
print(L1) #it prints the new asceding order into reverse bcoz sort changes into new list 

#Append the list
L1.append(2) #adds the no at the end of list
print(L1)

#Insert
L1.insert(3, 88) #adds the no into the 3rd position
print(L1) 

#Pop
L1.pop(2) #removes the value stored in 2nd position
print(L1)

#Remove
L1.remove(2)
L1.remove(2) #removes 2 from the list only the first from left to right
print(L1)

